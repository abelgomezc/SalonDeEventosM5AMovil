package xyz.abelgomez.navigationdrawer.model;

public class Documento {
    private String name;
    private String url;

    public Documento() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}

